let stats = ref({
  customers: 0,
  offices: 0,
  students: 0,
});

let counterRef = ref(false);

let visible = computed(() => (counterRef.value ? "positive" : "negative"));

const onIntersection = (entry) => {
  counterRef.value = entry.isIntersecting;
  if (visible.value === "positive") {
    startCountAnimation();
  } else {
    stats.value = {
      customers: 0,
      offices: 0,
      students: 0,
    };
  }
};

const startCountAnimation = () => {
  const duration = 10000000;
  const interval = 130;
  const finalCountCustomers = 6;
  const finalCountoffices = 5;
  const finalCountStudents = 20;
  const incrementCustomers = Math.round(
    finalCountCustomers / (duration / interval) + 1
  );
  const incrementOffices = Math.round(
    finalCountoffices / (duration / interval) + 1
  );
  const incrementStudents = Math.round(
    finalCountStudents / (duration / interval) + 1
  );
  const timer = setInterval(() => {
    if (stats.value.customers < finalCountCustomers) {
      stats.value.customers += incrementCustomers;
    } else {
      stats.value.customers = finalCountCustomers;
      clearInterval(timer);
    }
  }, interval);
  const timerPersonnel = setInterval(() => {
    if (stats.value.offices < finalCountoffices) {
      stats.value.offices += incrementOffices;
    } else {
      stats.value.offices = finalCountoffices;
      clearInterval(timerPersonnel);
    }
  }, interval);
  const timerFacility = setInterval(() => {
    if (stats.value.students < finalCountStudents) {
      stats.value.students += incrementStudents;
    } else {
      stats.value.students = finalCountStudents;
      clearInterval(timerFacility);
    }
  }, interval);
};
